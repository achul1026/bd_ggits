package com.neighbor21.ggits.common.entity;
public class GimsLogAccInfo {
    private String    accId;        //GIMS 접근 ID
    private String    accIp;        //GIMS 접근 IP
    private String    accDate;        //GIMS 접근 시간
    private String    accSigun;        //GIMS 접근 시군


  public String getAccId() {
    return accId;
  }

  public void setAccId(String accId) {
    this.accId = accId;
  }


  public String getAccIp() {
    return accIp;
  }

  public void setAccIp(String accIp) {
    this.accIp = accIp;
  }


  public String getAccDate() {
    return accDate;
  }

  public void setAccDate(String accDate) {
    this.accDate = accDate;
  }


  public String getAccSigun() {
    return accSigun;
  }

  public void setAccSigun(String accSigun) {
    this.accSigun = accSigun;
  }

}
