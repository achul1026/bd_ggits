package com.neighbor21.ggits.common.entity;
public class MOpOpenApiFilext {
    private String    filextId;        //확장자 아이디
    private String    apiId;        //API 아이디
    private String    filextNm;        //확장자 명
    private String    dataType;        //데이터 유형
    private String    fileId;        //파일 아이디


  public String getFilextId() {
    return filextId;
  }

  public void setFilextId(String filextId) {
    this.filextId = filextId;
  }


  public String getApiId() {
    return apiId;
  }

  public void setApiId(String apiId) {
    this.apiId = apiId;
  }


  public String getFilextNm() {
    return filextNm;
  }

  public void setFilextNm(String filextNm) {
    this.filextNm = filextNm;
  }


  public String getDataType() {
    return dataType;
  }

  public void setDataType(String dataType) {
    this.dataType = dataType;
  }


  public String getFileId() {
    return fileId;
  }

  public void setFileId(String fileId) {
    this.fileId = fileId;
  }

}
